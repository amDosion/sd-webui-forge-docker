        return
    fi

    # æ£€æŸ¥æ˜¯å¦å¯ç”¨äº† HF é•œåƒä»¥åŠæ˜¯å¦æ˜¯ Hugging Face URL
    # ä½¿ç”¨æ­¥éª¤ [2] ä¸­å®šä¹‰çš„ HF_MIRROR_URL
