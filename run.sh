    # æ£€æŸ¥æ˜¯å¦å¯ç”¨äº† Git é•œåƒä»¥åŠæ˜¯å¦æ˜¯ GitHub URL
    if [[ "$USE_GIT_MIRROR" == "true" && "$repo_original" == "https://github.com/"* ]]; then
