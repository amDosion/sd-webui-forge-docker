        git_mirror_host=$(echo "$GIT_MIRROR_URL" | sed 's|https://||; s|http://||; s|/.*||')
        repo_url=$(echo "$repo_original" | sed "s|github.com|$git_mirror_host|")
        echo "    - ä½¿ç”¨é•œåƒè½¬æ¢ (Git): $repo_original -> $repo_url"
