        download_url="$url_original"
    fi

    # æ£€æŸ¥æ–‡ä»¶æ˜¯å¦å·²å­˜åœ¨
    if [ ! -f "$output_path" ]; then
        echo "    - â¬‡ï¸ ä¸‹è½½ ${type}: $filename (from $download_url)"
